package com.jmulla.some10000words.JSONModels;

public class DEWordPair {

  private String de;
  private String en;

  public DEWordPair(String de, String en) {
    this.de = de;
    this.en = en;
  }

  public String getDe() {
    return de;
  }

  public void setDe(String de) {
    this.de = de;
  }

  public String getEn() {
    return en;
  }

  public void setEn(String en) {
    this.en = en;
  }
}
